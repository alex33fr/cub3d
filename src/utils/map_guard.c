#include "graphic.h"

int	is_wall(t_game *g, int x, int y)
{
	if (x < 0 || x >= g->map_w || y < 0 || y >= g->map_h)
		return (1);
	if (g->map[y][x] == ' ' || g->map[y][x] == '1')
		return (1);
	return (0);
}
